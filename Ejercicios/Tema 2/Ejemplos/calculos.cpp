#include <iostream>
using namespace std;

int main()
{
   cout << "133 + 1234 = " << 133 + 1234 << endl;
   cout << "1234 - 111.5 = " << 1234 - 111.5 << endl;
   cout << "34 * 59 = " << 34 * 59 << endl;
   cout << "3.4 * 5.93 = " << 3.4 * 5.93 << endl;
   cout << "500 / 3 = " << 500 / 3 << endl; // División entera
   cout << "500.0 / 3 = " << 500.0 / 3 << endl; // División real

   return 0;
}
